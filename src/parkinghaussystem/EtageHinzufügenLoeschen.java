    public void updateEtagenTable() {
        DefaultTableModel model = (DefaultTableModel) etagenTable.getModel();
        model.setRowCount(0); // Löscht alle vorhandenen Zeilen

        List<String> etagenNamen = DBOperations.getAlleEtagenNamen();

        for (String name : etagenNamen) {
            model.addRow(new Object[]{name}); // Nehmen Sie an, dass die Tabelle jetzt nur eine Spalte hat
        }
    }


    private void btnAnmeldenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnmeldenActionPerformed
        // TODO add your handling code here:
        String email = txtEmail.getText().trim();
        String kennwort = txtKennwort.getText();
        boolean istAuthentifiziert = DBOperations.authentifiziereAdmin(email, kennwort);

        if (!istAuthentifiziert) {

            JOptionPane.showMessageDialog(null, "<html><b style=\"color:red\">Falscher Benutzername oder falsches Passwort</b></html>", "Fehler", JOptionPane.ERROR_MESSAGE);
        } else {

            JOptionPane.showMessageDialog(null, "<html><b>Anmeldung erfolgreich</b></html>", "Erfolg", JOptionPane.INFORMATION_MESSAGE);
            AdminVerwaltung adminVerwaltung = new AdminVerwaltung(); // Ersetzen Sie dies durch Ihr tatsächliches Dashboard-Fenster
            adminVerwaltung.setVisible(true);
            this.dispose(); // Schließt das aktuelle Anmeldefenster
        }
    }//GEN-LAST:event_btnAnmeldenActionPerformed


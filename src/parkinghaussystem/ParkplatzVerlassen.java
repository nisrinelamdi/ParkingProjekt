    // Überprüft die Textfelder und aktiviert/deaktiviert den "Verlassen"-Button.
    public void validateFields() {
        String vorname = txtVorname.getText();
        String nachname = txtNachname.getText();
        String nummerschild = txtSchildsnummer.getText();

        if (!vorname.equals("") && (!nachname.equals("")) && !nummerschild.equals("")) {
            btnVerlassen.setEnabled(true);
        } else {
            btnVerlassen.setEnabled(false);

        }
    }


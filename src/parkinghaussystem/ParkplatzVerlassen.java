    private void btnVerlassenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerlassenActionPerformed
        // TODO add your handling code here:
        String nummernschild = txtSchildsnummer.getText();

        if (nummernschild.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bitte geben Sie die Schildnummer ein.", "Fehler", JOptionPane.WARNING_MESSAGE);
            return;
        }

        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String ausfahrZeitpunkt = dateFormat.format(new Date());

        boolean erfolgreich = DBOperations.verlassenParkplatz(nummernschild, ausfahrZeitpunkt);

        if (erfolgreich) {
            JOptionPane.showMessageDialog(this, "Der Parkplatz wurde erfolgreich verlassen.", "Erfolg", JOptionPane.INFORMATION_MESSAGE);
            resetFormular();
        } else {
            JOptionPane.showMessageDialog(this, "Fehler beim Verlassen des Parkplatzes oder das Fahrzeug wurde nicht gefunden.", "Fehler", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnVerlassenActionPerformed


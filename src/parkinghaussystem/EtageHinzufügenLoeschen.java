    private void etagenTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_etagenTableMouseClicked
        // TODO add your handling code here:
        int index = etagenTable.getSelectedRow();
        TableModel model = etagenTable.getModel();
        int id = Integer.parseInt(model.getValueAt(index, 0).toString());
        String name = model.getValueAt(index, 1).toString();
        int a = JOptionPane.showConfirmDialog(null, "Wollen Sie wirklich löschen " + name + " Etage", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            boolean erfolg = DBOperations.deleteEtage(id);
            if (erfolg) {
                JOptionPane.showMessageDialog(null, "Etage wurde gelöscht.");

                ((DefaultTableModel) model).removeRow(index);
            } else {
                JOptionPane.showMessageDialog(null, "Fehler beim Löschen der Etage.");
            }
        }
    }//GEN-LAST:event_etagenTableMouseClicked


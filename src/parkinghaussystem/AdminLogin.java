    public void validateFields() {
        String email = txtEmail.getText();
        String kennwort = txtKennwort.getText();
        if (email.matches(emailPattern) && !kennwort.equals("")) {
            btnAnmelden.setEnabled(true);
        } else {
            btnAnmelden.setEnabled(false);

        }
    }

